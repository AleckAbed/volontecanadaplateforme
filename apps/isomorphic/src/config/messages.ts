type Locale = 'fr' | 'en' | 'es';

function getLocale(): Locale {
  if (typeof window === 'undefined') return 'fr';
  try {
    const v = localStorage.getItem('app_language');
    if (v === 'fr' || v === 'en' || v === 'es') return v;
  } catch {}
  return 'fr';
}

