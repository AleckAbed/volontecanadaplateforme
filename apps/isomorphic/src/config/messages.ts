type MessagesShape = typeof messagesByLocale.fr;

/**
 * Proxy qui retourne la traduction pour la locale courante.
 * Lit `localStorage.app_language` à chaque accès — mais les valeurs capturées
 * dans les schémas Zod au module-load restent celles de la locale initiale.
 *
 * Pour s'assurer que les schémas utilisent la bonne locale, il faut rafraîchir
 * la page après changement de langue.
 */
export const messages = new Proxy({} as MessagesShape, {
  get(_target, prop: string) {
    const locale = getLocale();
    const dict = messagesByLocale[locale] || messagesByLocale.fr;
    return (dict as any)[prop];
  },
});

/** Helper pour accéder à une langue spécifique (utile pour les tests ou usage avancé). */
export function getMessages(locale: Locale): MessagesShape {
  return messagesByLocale[locale] || messagesByLocale.fr;
}
